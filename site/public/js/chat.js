var show_lit = false
function literate() {
    if (show_lit == true) {
        show_lit = false
        lit_dropdown.style.display = 'none'
    } else {
        show_lit = true
        lit_dropdown.style.display = 'block'
    }
}

function clickclose3() {
    if (show_lit == true) {
        show_lit = false
        lit_dropdown.style.display = 'none'
}
}
